import re
txt = input()

# Напишите программу на Python, которая совпадает со строкой с нулём или более 'a''b'
x = re.search(r"ab*", txt)
print(x.group())

# Напишите программу на Python, которая совпадает со строкой, с которой следуют два или три 'a''b'
x = re.search(r"ab{2,3}", txt)
print(x.group())

# Напишите программу на Python, чтобы найти последовательности строчных букв, соединённых с подчёркнутым.
x = re.findall(r"[a-z]_[a-z]", txt)
print(*x)

# Напишите программу на Python, чтобы найти последовательности одной заглавной буквы, за которой следуют строчные буквы.
x = re.findall(r"[A-Z][a-z]*", txt)
print(*x)

# Напишите программу на Python, которая совпадает со строкой, у которой есть , с которым следует что-либо, заканчиваясь на 'a''b'
x = re.findall(r"^a.*b$", txt)
print(*x)

# Напишите программу на Python, чтобы заменить все появления пробела, запятой или точки двоеточием.
print(re.sub(r"[ ,\.]", ":", txt))

# Напишите программу на Python для преобразования строки snake case в строку camel case.
parts = [p for p in txt.split("_") if p]
if not parts:
    print("")
else:
    print(parts[0].lower() + "".join(p[:1].upper() + p[1:].lower() for p in parts[1:]))

# Напишите программу на Python, чтобы разбить строку на заглавные буквы.
parts = [p for p in re.split(r"(?=[A-Z])", txt) if p]
print(parts)

# Напишите программу на Python, чтобы вставлять пробелы между словами, начинающиеся с заглавных букв.
print(re.sub(r"(?<!^)(?=[A-Z])", " ", txt).strip())

# Напишите программу на Python, которая преобразует заданную строку верблюда в snake case.
print(re.sub(r"(?<!^)(?=[A-Z])", "_", txt).lower())